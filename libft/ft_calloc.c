#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	void	*mem;

	mem = malloc(size * count);
	if (!mem)
		return (NULL);
	else
	{
		ft_bzero(mem, count * size);
		return (mem);
	}
}
